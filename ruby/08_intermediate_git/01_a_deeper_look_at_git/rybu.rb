=begin
  1.- Explain what it means for branches to be pointers.

  Branches point to commits, and that's how they can be created so quickly.
=end

=begin
  2.- How can you amend your last commit?

  First we change and stage the files we want to add to the last commit, then we run `git commit --amend`, and update the commit message.
=end

=begin
  3.- What are some different ways to rewrite history?

  With amend and rebase.
=end
