=begin
  1.- What is a variable?
  
  A variable is something that holds a value.
=end

=begin
  2.- How do you assign a value or an expression to a variable?

  With the = operator, by placing the variable on the left, and the value or expression on the right.
=end

=begin
  3.- What does the += operator do?

  It attempts to set the value of the variable on the left to the result of calling + operator on the variable with the value on
  the right as the parameter.
=end

=begin
  4.- What does the -= operator do?

  It attempts to set the value of the variable on the left to the result of calling - operator on the variable with the value on
  the right as the parameter.
=end

=begin
  5.- What does the *= operator do?

  It attempts to set the value of the variable on the left to the result of calling * operator on the variable with the value on
  the right as the parameter.
=end

=begin
  6.- What does the /= operator do?


  It attempts to set the value of the variable on the left to the result of calling / operator on the variable with the value on
  the right as the parameter.
=end

=begin
  7.- What are the variable naming conventions?

  Variable names should be descriptive of what they are used for so other people can easily understand the code.
=end
