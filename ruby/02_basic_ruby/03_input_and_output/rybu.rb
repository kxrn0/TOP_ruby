=begin
  1.- What are the two methods we can use to output data to the user's screen?
  
  We can use puts and print to print data to the console.
=end

=begin
  2.- What is similar between these two output methods?

  They both output a representation of the data we feed them to the console, and they both return nil.
=end

=begin
  3.- What is different between these two output methods?

  puts tries to convert everything to a string, so nil will be converted to "", while print tries to print a version of the
  object that looks more like itself, for example nil is printed as nil. puts adds a newline character after printing, print
  doesn't.
=end

