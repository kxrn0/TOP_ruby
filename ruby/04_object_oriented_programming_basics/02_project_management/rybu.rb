=begin
  4.- What are gems?

  Other's people code that is hosted by rubygems.org, from where it can be downloaded.
=end

=begin
  5.- How would you install gems?

  With gem install <gem_name> for global, and bundle add <gem_name> in a bundled project.
=end

=begin
  6.- What is bundler used for?

  To manage dependencies by taking care of managing versioning.
=end

=begin
  7.- Why would you use bundle exec?

  To run specifically the gem versions in the Gemfile.
=end

=begin
  8.- What are Gemfile and Gemfile.lock for?

  For declaring the dependencies used in a project, and their supported versions.
=end
