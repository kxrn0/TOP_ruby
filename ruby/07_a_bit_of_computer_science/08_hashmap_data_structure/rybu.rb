=begin
  1.- What does it mean to hash?

  Feeding a piece of information to a pure function, and getting back some result. Hashing is often assumed to be a non reversible process.
=end

=begin
  2.- What are buckets?

  Where the information is stored in the hash table.
=end

=begin
  3.- What is a collision?

  When two different pieces of information generate the same hash.
=end

