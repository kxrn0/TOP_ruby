=begin
  1.- What is Big O?

  A way to analyze how much time or resources an algorithm will take when running.
=end

=begin
  2.- What are the Big O notations?

  Big O: It gives the wost case scenario.
  Big Omega: It gives the best case scenario.
  Big Theta: It gives the average.
=end

=begin
  3.- Why use Big O?

  To get a rough estimate of how much input the system can cope with.
=end

=begin
  4.- What is Big Omega, and why isn't as useful?

  It gives the best case scenario for when running an algorithm. It's not as useful because it assumes an ideal case, which is not as common in the real world.
=end
